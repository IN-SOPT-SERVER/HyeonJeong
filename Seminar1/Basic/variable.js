// var 변수 - 재선언, 재할당 가능
var myName = "hyeon";
console.log(myName);

var myName = "jeong";
console.log(myName);

// let 변수 - 재선언 가능
let myLetName = "jeong";
myLetName = "쪙";
console.log(`My Name is ${myLetName}`);

// const 변수 - 재선언, 재할당 불가능
const myConstName = "HyeonJeong";
console.log(`My Name is ${myConstName}`);
